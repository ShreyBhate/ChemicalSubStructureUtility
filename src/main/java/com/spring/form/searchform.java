package com.spring.form;

public class searchform {

	
	private String molstructure;
	private String filterName;
	private String name;
	private String smilesName;
	private String searchOption;

	
	public String getMolstructure() {
		return molstructure;
	}

	public void setMolstructure(String molstructure) {
		this.molstructure = molstructure;
	}

	public String getFilterName() {
		return filterName;
	}

	public void setFilterName(String filterName) {
		this.filterName = filterName;
	}

	

	public String getSmilesName() {
		return smilesName;
	}

	public void setSmilesName(String smilesName) {
		this.smilesName = smilesName;
	}

	public String getSearchOption() {
		return searchOption;
	}

	public void setSearchOption(String searchOption) {
		this.searchOption = searchOption;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
