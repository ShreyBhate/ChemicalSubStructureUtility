package com.spring.vo;

public class ArticleVo {

	private String ID;	
	private String TITLE;
	private String ABSTRACT;
	private String INTRODUCTION;
	private String EXPERIMENTAL_SECTION;
	private String RESULTS_DISCUSSION;
	private String CONCLUSION;
	private String REFERENCE;
	private String NAME;
	private int COMPID;
	private String DOI;
	private String FULLTEXT;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getTITLE() {
		return TITLE;
	}
	public void setTITLE(String tITLE) {
		TITLE = tITLE;
	}
	public String getABSTRACT() {
		return ABSTRACT;
	}
	public void setABSTRACT(String aBSTRACT) {
		ABSTRACT = aBSTRACT;
	}
	public String getINTRODUCTION() {
		return INTRODUCTION;
	}
	public void setINTRODUCTION(String iNTRODUCTION) {
		INTRODUCTION = iNTRODUCTION;
	}
	public String getEXPERIMENTAL_SECTION() {
		return EXPERIMENTAL_SECTION;
	}
	public void setEXPERIMENTAL_SECTION(String eXPERIMENTAL_SECTION) {
		EXPERIMENTAL_SECTION = eXPERIMENTAL_SECTION;
	}
	public String getRESULTS_DISCUSSION() {
		return RESULTS_DISCUSSION;
	}
	public void setRESULTS_DISCUSSION(String rESULTS_DISCUSSION) {
		RESULTS_DISCUSSION = rESULTS_DISCUSSION;
	}
	public String getCONCLUSION() {
		return CONCLUSION;
	}
	public void setCONCLUSION(String cONCLUSION) {
		CONCLUSION = cONCLUSION;
	}
	public String getREFERENCE() {
		return REFERENCE;
	}
	public void setREFERENCE(String rEFERENCE) {
		REFERENCE = rEFERENCE;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public int getCOMPID() {
		return COMPID;
	}
	public void setCOMPID(int cOMPID) {
		COMPID = cOMPID;
	}
	public String getDOI() {
		return DOI;
	}
	public void setDOI(String dOI) {
		DOI = dOI;
	}
	public String getFULLTEXT() {
		return FULLTEXT;
	}
	public void setFULLTEXT(String fULLTEXT) {
		FULLTEXT = fULLTEXT;
	}


}